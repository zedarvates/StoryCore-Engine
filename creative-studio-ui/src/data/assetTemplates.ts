// ============================================================================
// Helper Functions
// ============================================================================

export const getAssetTemplateById = (id: string): AssetTemplate | undefined => {
  return assetTemplates.find(template => template.id === id);
};

export const getAssetTemplatesByCategory = (category: string): AssetTemplate[] => {
  // Asset templates use tags for categorization instead of a single category field
  return assetTemplates.filter(template => template.tags.includes(category));
};

export const getAllAssetTemplates = (): AssetTemplate[] => {
  return [...assetTemplates];
};