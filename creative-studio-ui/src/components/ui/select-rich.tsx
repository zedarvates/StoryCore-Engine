// ============================================================================
// Custom SelectItem with Rich Content Support
// ============================================================================

export interface SelectItemWithDescriptionProps extends React.ComponentPropsWithoutRef<typeof SelectPrimitive.Item> {
  /** The text value to display when selected */
  value: string;
  /** The label text (shown in dropdown and as value) */
  label: string;
  /** Optional icon to display in the dropdown */
  icon?: React.ReactNode;
  /** Optional description text to display in the dropdown */
  description?: string;
  /** Optional CSS class for additional styling */
  className?: string;
}

/**
 * SelectItemWithDescription Component
 * 
 * A wrapper around Radix UI SelectItem that supports:
 * - Icon display in the dropdown
 * - Description text in the dropdown
 * - Proper text content for the select value (fixes React error #31)
 * 
 * Usage:
 * <SelectItemWithDescription value="protagonist" label="Protagonist" icon="â­" description="Main character" />
 */
const SelectItemWithDescription = React.forwardRef<
  React.ElementRef<typeof SelectPrimitive.Item>,
  SelectItemWithDescriptionProps
>(({ className, children, value, label, icon, description, ...props }, ref) => {
  return (
    <SelectPrimitive.Item
      ref={ref}
      className={cn(
        "relative flex w-full cursor-default select-none items-center rounded-sm py-1.5 pl-8 pr-2 text-sm outline-none focus:bg-accent focus:text-accent-foreground data-[disabled]:pointer-events-none data-[disabled]:opacity-50",
        className
      )}
      value={value}
      {...props}
    >
      <span className="absolute left-2 flex h-3.5 w-3.5 items-center justify-center">
        <SelectPrimitive.ItemIndicator>
          <Check className="h-4 w-4" />
        </SelectPrimitive.ItemIndicator>
      </span>

      {/* The text content for the select value - MUST be simple text */}
      <SelectPrimitive.ItemText>
        {icon && <span className="inline-flex mr-1">{icon}</span>}
        {label}
      </SelectPrimitive.ItemText>

      {/* Rich content for dropdown display (positioned behind/around the text) */}
      {icon || description ? (
        <div 
          className={cn(
            "flex flex-col",
            icon && description ? "gap-0.5" : "justify-center"
          )}
        >
          {icon && (
            <span className="absolute left-7 top-1/2 -translate-y-1/2 text-lg">
              {icon}
            </span>
          )}
          {description ? (
            <span className="text-xs text-muted-foreground mt-0.5">
              {description}
            </span>
          ) : null}
        </div>
      ) : null}
    </SelectPrimitive.Item>
  )
})
SelectItemWithDescription.displayName = SelectPrimitive.Item.displayName

